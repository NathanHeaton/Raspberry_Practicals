gap = 0.008

def CHRD(notes, duration):
    index = 0
    noteIndex = 0
    chord =[]
    while index < duration:
        if noteIndex >= len(notes):
            noteIndex = 0
        note = notes[noteIndex]
        
        chord.append((note,gap))
        
        index += gap
        noteIndex+=1
        
    return chord

